TEST(Primality, HandlesSmallEdges) {
  EXPECT_FALSE(campaign::is_prime(0));
  EXPECT_FALSE(campaign::is_prime(1));
  EXPECT_TRUE(campaign::is_prime(2));
  EXPECT_TRUE(campaign::is_prime(3));
  EXPECT_FALSE(campaign::is_prime(4));
}

