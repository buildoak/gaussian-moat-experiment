TEST(Primality, CarmichaelNumbersAreComposite) {
  constexpr std::array<std::uint64_t, 4> kCarmichael = {
      561ULL,
      41041ULL,
      825265ULL,
      321197185ULL,
  };

  for (const std::uint64_t n : kCarmichael) {
    EXPECT_FALSE(campaign::is_prime(n)) << n;
  }
}

TEST(Primality, DeterministicAcrossRepetitions) {
  constexpr std::array<std::uint64_t, 6> kCases = {
      2ULL,
      561ULL,
      1000003ULL,
      1738507ULL,
      321197185ULL,
      1001311ULL,
  };

  for (const std::uint64_t n : kCases) {
    const bool expected = campaign::is_prime(n);
    EXPECT_EQ(campaign::is_prime(n), expected) << n;
    EXPECT_EQ(campaign::is_prime(n), expected) << n;
    EXPECT_EQ(campaign::is_prime(n), expected) << n;
  }
}

